/*
*********************************************************************************************************
* Description : BSP初始化方法。
*
* Caller(s)   : none.
*
* Note(s)     : none.
*********************************************************************************************************
*/
void BSP_Init(void) large
{
    AUXR |= 0x80;       // 定时器T0设置为1T（为兼容传统8051，默认12T）
    EA = 1;             // 使能总中断。

    // 初始化
    BSP_SEG_Init();
    BSP_KBD_Init();
    BSP_BUZZER_Init();
    BSP_WELDER_Init();
    BSP_EEPROM_Init();
    BSP_UART_Init();
    BSP_POWER_Init();
}

